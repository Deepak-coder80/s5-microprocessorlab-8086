DATA SEGMENT
	MSG1 DB "ENTER  STRING A : $"
	MSG2 DB 0DH,0AH,"ENTER REPLACE CHARACTER : $"
	MSG3 DB 0DH,0AH,"ENTER  CHARACTER REPLACER : $"
	MSG4 DB 0DH,0AH,"FINAL STRING : $"
	A DB 255 DUP('$')
	
	
	LEN DW 0000H
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	START:
	MOV AX,DATA
	MOV DS,AX
	MOV ES,AX
	
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	
	LEA SI,A
	MOV AH,01H
	CALL READ
	
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	
	MOV AH,01H
	INT 21H
	
	MOV CL,AL
	
	LEA DX,MSG3
	MOV AH,09H
	INT 21H
	
	MOV AH,01H
	INT 21H
	
	MOV CH,AL
	
	LEA SI,A
	
	MOV AL,"$"
	NEXT: CMP [SI],AL
	JZ EXIT 
	INC LEN
	CMP [SI],CL
	JNZ EXIT2
	MOV [SI],CH
	EXIT2: INC SI
	JMP NEXT

	EXIT: LEA DX,MSG4
	MOV AH,09H
	INT 21H
	
	LEA DX,A
	MOV AH,09H
	INT 21H
	
	
	MOV AH,4cH
	INT 21H

READ PROC NEAR
	LOOP1: INT 21H
	MOV [SI],AL
	INC SI
	CMP AL,0DH
	JNE LOOP1
	RET 
READ ENDP

CODE ENDS
END START

