DATA SEGMENT
	MSG1 DB "ENTER  STRING A : $"
	MSG2 DB 0DH,0AH,"ENTER REPLACE STRING : $"
	MSG3 DB 0DH,0AH,"AFTER REPLACE STRING A : $"
	A DB 255 DUP('$')
	B DB 255 DUP('$')
	
	LEN DW 0000H
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	START:
	MOV AX,DATA
	MOV DS,AX
	MOV ES,AX
	
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	
	LEA SI,A
	MOV AH,01H
	CALL READ
	
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	
	LEA SI,B
	MOV AH,01H
	CALL READ
	
	LEA SI,B
	
	
	MOV AL,"$"
	NEXT1:CMP [SI],AL
		JZ EXIT
		INC LEN
		INC SI
		JMP NEXT1
		
	EXIT: DEC LEN
	LEA SI,B
	LEA DI,A
	LEA CX,LEN
	CLD
	REP MOVSB
	MOV AL,"$"
	MOV [SI],AL
	
	
	LEA DX,MSG3
	MOV AH,09H
	INT 21H
	
	LEA DX,A
	MOV AH,09H
	INT 21H
	
	
	
	MOV AH,4cH
	INT 21H

READ PROC NEAR
	LOOP1: INT 21H
	MOV [SI],AL
	INC SI
	CMP AL,0DH
	JNE LOOP1
	RET 
READ ENDP

CODE ENDS
END START

