DATA SEGMENT
	MSG1 DB "ENTER FIRST STRING : $"
	MSG2 DB 0DH,0AH,"ENTER SECOND STRING : $"
	MSG3 DB 0DH,0AH,"CONCATINATED STRING : $"
	A DB 255 DUP('$')
	B DB 255 DUP('$')
	
	LEN DB 00H
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	START:
	MOV AX,DATA
	MOV DS,AX
	
	
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	
	LEA SI,A
	MOV AH,01H
	CALL READ
	
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	
	LEA SI,B
	MOV AH,01H
	CALL READ
	
	LEA SI,A
	LEA DI,B
	
	MOV AL,"$"
	NEXT1:CMP [SI],AL
		JZ EXIT
		INC LEN
		INC SI
		JMP NEXT1
		
	EXIT: MOV BL,"$"
	DEC LEN
	DEC SI
	NEXT: MOV AL,[DI]
	CMP AL,BL
	JZ EXIT1
	MOV [SI],AL
	INC SI
	INC DI
	JMP NEXT
	EXIT1:MOV [SI],BL;
	
	
	LEA DX,MSG3
	MOV AH,09H
	INT 21H
	
	LEA DX,A
	MOV AH,09H
	INT 21H
	
	
	
	MOV AH,4cH
	INT 21H

READ PROC NEAR
	LOOP1: INT 21H
	MOV [SI],AL
	INC SI
	CMP AL,0DH
	JNE LOOP1
	RET 
READ ENDP

CODE ENDS
END START

